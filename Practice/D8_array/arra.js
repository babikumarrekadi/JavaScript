// Array Methods :
//             Array methods are built-in functions used to add, remove, search, modify, and loop through arrays.


let mobiles=["oneplus","apple","samsung","vivo","oppo","nokia","docomo","redmi","realme","poco"]

// ➤ Length() – Number of elements in an array.
console.log(mobiles.length);

// ➤ indexOf()
console.log(mobiles.indexOf("redmi"));

// ➤ includes()
console.log(mobiles.includes("apple"));

// ➤ concat()
b=[5,4]
console.log(mobiles.concat(b));

// ➤ push() – Add to end
mobiles.push("iqoo")
console.log(mobiles);

// ➤ pop() – Remove from end
mobiles.pop()
console.log(mobiles);

// ➤ unshift() – Add to beginning
mobiles.unshift("iqoo")
console.log(mobiles);

// ➤ shift() – Remove from beginning
mobiles.shift()
console.log(mobiles);




